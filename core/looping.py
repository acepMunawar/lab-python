
#====================== For Loop

# Iteration number

for i in range(1,5):
    print(i)

print("")

# Iteration Order

buah = ["apel","jeruk","mangga"]

for item in buah:
    print(item) 

print("")

# Loop dua dimensi

for i in range(3):
    for j in range(2):
        print(i, j)

print("")

#====================== For While

x =1

while x <= 10:
    print(x)
    x += 1